# Pacote UI
#
# Este pacote contém componentes visuais, painéis e widgets usados na interface do Vox Imago.
# Exemplos: visualização de listas, painel de detalhes, miniaturas, widgets personalizados, etc.
#
# Módulos deste pacote:
# - list_view.py: Visualização personalizada de listas de arquivos.
# - list_model.py: Modelo de dados para listas de arquivos.
# - details_panel.py: Painel de detalhes do arquivo.
# - thumbnails.py: Gerenciamento e cache de miniaturas.
# - widgets.py: Widgets personalizados para a interface.
